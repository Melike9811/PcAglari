class Taslar {

    private int x;
    private int y;
    private int flag;

    public Taslar(int x, int y, int flag) {
        this.x = x;
        this.y = y;
        this.flag = flag;
    }

    public int getx() {
        return this.x;
    }

    public int gety() {
        return this.y;
    }

    public int getflag() {
        return this.flag;
    }

}
